/*
As variáveis em Javascript não tem um tipo definido na sua declaração, o tipo delas é
definido quando ocorre uma atribuição de valor.
*/

var inteiro = 2;
var flutuante = 10.7;
var texto = "Essa variável é uma string";
var booleano = true; //true=verdadeiro false=falso


/*
Para visualizar as saídas basta incluir esse arquivo em documento html
e visualizar o console no navegador.
*/
console.log(inteiro);
console.log(flutuante);
console.log(texto);
console.log(booleano);
